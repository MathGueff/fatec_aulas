# Calculando a área de um quadrilátero com base e altura diferentes
print("Vamos calcular a área desse quadrilátero")

base = float(input("Digite o valor da base: "))
altura = float(input("Digite o valor da altura: "))
uni = input("Digite a unidade de medida: ")

area = base * altura

print(f"A área do quadrilátero, sendo a sua base {base}{uni} e altura {altura}{uni}, é {area}{uni}²")

